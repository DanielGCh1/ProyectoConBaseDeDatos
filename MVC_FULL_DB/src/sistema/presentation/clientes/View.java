    @Override
    public void update(Observable o, Object arg) {
        Cliente cliente = model.getCliente();
        cedula.setText(cliente.getCedula());
        nombre.setText(cliente.getNombre());
        telefono.setText(cliente.getTelefono());
        int prov;
        if (cliente.getCedula().isEmpty()) prov=0;
        else prov=Integer.parseInt(cliente.getCedula().substring(0, 1));
        flag.setIcon(flags[prov]);
        clientes.setModel(new ClientesTableModel(model.getClientes()));
        
    }
