   int n=8;
   ImageIcon[] flags;        
    public void loadFlags() {
        flags = new ImageIcon[n];
        try {
            for(int p=0;p<n;p++) flags[p] = new ImageIcon(ImageIO.read(getClass().getResourceAsStream("../flags/bandera"+p+".png"))); 
        } catch (IOException e) {
            System.err.println(e);
        }
    }  
